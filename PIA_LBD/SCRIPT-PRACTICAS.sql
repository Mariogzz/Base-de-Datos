CREATE FUNCTION IVA_DEL_SUBTOTAL (@param1 money)
RETURNS money
AS BEGIN
	DECLARE @IVA money
	SET @IVA = @param1 * 0.16
    RETURN @IVA
END
SELECT dbo.IVA_DEL_SUBTOTAL(400) AS IVA_IMPORTE

CREATE FUNCTION TOTAL_CUENTA (@subtotal money)
RETURNS money
AS BEGIN
	DECLARE @TOTAL_IVA money
	DECLARE @TOTAL money
	SET @TOTAL_IVA = @subtotal * 1.16
	SET @TOTAL = @TOTAL_IVA*1.1
    RETURN @TOTAL
END
SELECT dbo.TOTAL_CUENTA (1000) AS TOTAL_CUENTA

CREATE FUNCTION INVENTARIO_BAJO(@ProductoID smallint)  
RETURNS bit   
AS
BEGIN  
    DECLARE @ret smallint;
    DECLARE @nivel bit;  
    SELECT @ret = CANTIDAD FROM ALMACEN WHERE PRODUCTO_ID = @ProductoID;  
    IF (@ret < 20)   
        SET @nivel = 1;
    IF (@ret >= 20)   
        SET @nivel = 0;  
    RETURN @nivel;  
END; 
SELECT dbo.INVENTARIO_BAJO(4) AS REABASTECER

CREATE FUNCTION IVA_DEL_IMPORTE (@subtotal money)
RETURNS money
AS BEGIN
	DECLARE @IMPORTE money
	SET @IMPORTE = @subtotal * 1.16
    RETURN @IMPORTE
END
SELECT dbo.IVA_DEL_IMPORTE(500) AS IMPORTE_IVA


CREATE FUNCTION INVENTARIO_PRODUCTOS()  
RETURNS int   
AS
BEGIN    	
	DECLARE @SUM int    
	SELECT @SUM = SUM(CANTIDAD) FROM ALMACEN WHERE ALMACEN_ID BETWEEN 1 and 1000;
    RETURN @SUM;  
END; 
SELECT dbo.INVENTARIO_PRODUCTOS() AS TOTAL_PRODUCTOS_ALMACEN

CREATE FUNCTION CONVERT_USD_MXN(@usd money)
RETURNS money
AS BEGIN
	DECLARE @mxn money
	SET @mxn = @usd * 18.5
    RETURN @mxn
END
SELECT dbo.CONVERT_USD_MXN(5) AS USD_MXN


